			<p>If you're considering investing in real estate, there are several options to consider. One option is to rent out your property on platforms like Airbnb. While it's not required, it's a good idea to have a license agreement in place to protect your rights as a homeowner. This can help safeguard against damages, illegal use of the property, and other potential risks.</p>

			<p><b>Another option is to invest in multifamily homes,</b> where you can live in one unit and rent out the other. Or, you could rent out a portion of your home, like a floor or half of the house. This can be a good way to cover your mortgage and even earn extra income.</p>


			<p>Another option is real estate investment trusts, which are regulated and allow you to own large tracts of property or share in such an investment. However, these trusts have not yet taken off in Kenya.</p>

			<p>Traditional methods of real estate investing, such as buying and selling property and leasing, are also still popular. There are four main types of leases: residential, commercial, industrial, and hybrid. It's important to understand the differences between these types and the legal documentation and provisions that apply to each. </p>

			<p>As a property investor, you have certain rights, including charging interest, attaching assets, and evicting tenants if necessary. Property rights are strong in Kenya, and the Constitution protects against arbitrary deprivation of property. Overall, real estate investing can be a safe and lucrative option.</p>

